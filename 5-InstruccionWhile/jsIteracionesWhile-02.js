function mostrar()
{
	let contador;
	
	contador=10; 

	while(contador>0)//2. segunda parte de la iteracion
	{
		alert(contador);
		contador=contador-1;//3. tercera parte de la iteracion
	}	
	//alert('iteración while');

}//FIN DE LA FUNCIÓN